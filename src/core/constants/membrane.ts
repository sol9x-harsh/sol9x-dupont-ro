// ─── Resolution helpers ───────────────────────────────────────────────────────

/**
 * Classify feed water type based on TDS.
 *   > 20,000 mg/L → SWRO
 *   > 500 mg/L → BWRO
 *   else → NF
 */
export function classifyWaterType(feedTDSMgL: number): WaterType {
  if (feedTDSMgL > 20000) return "swro";
  if (feedTDSMgL > 500) return "bwro";
  return "nf";
}

/**
 * Resolve membrane engineering properties from model name.
 * Falls back to TDS-based default when model is not found in catalog.
 */
export function resolveMembraneProperties(
  modelName: string | undefined | null,
  feedTDSMgL: number = 0,
): MembraneDefaults {
  if (modelName && MEMBRANE_CATALOG[modelName]) {
    return MEMBRANE_CATALOG[modelName];
  }
  // Fallback: classify by feed TDS
  const wt = classifyWaterType(feedTDSMgL);
  if (wt === "swro") return MEMBRANE_DEFAULTS_SEAWATER;
  if (wt === "nf") return MEMBRANE_DEFAULTS_NF;
  return MEMBRANE_DEFAULTS_BRACKISH;
}

/** Returns all catalog entries sorted A-Z within each waterType group. */
export function getMembraneCatalogByCategory(): {
  sw: MembraneCatalogEntry[];
  bw: MembraneCatalogEntry[];
  nf: MembraneCatalogEntry[];
} {
  const entries = Object.values(MEMBRANE_CATALOG);
  const sort = (arr: MembraneCatalogEntry[]) =>
    arr.sort((a, b) => a.label.localeCompare(b.label));
  return {
    sw: sort(entries.filter((e) => e.waterType === "swro")),
    bw: sort(entries.filter((e) => e.waterType === "bwro")),
    nf: sort(entries.filter((e) => e.waterType === "nf")),
  };
}
